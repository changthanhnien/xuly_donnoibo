export function lookupProductName(sku: string): string {
  if (!sku) return "Chưa xác định";
  const cleanSku = sku.trim();
  
  // 1. Direct exact lookup
  if (PRODUCT_MAPPING[cleanSku]) {
    return PRODUCT_MAPPING[cleanSku];
  }

  // 2. Case-insensitive lookup
  const upperSku = cleanSku.toUpperCase();
  const keys = Object.keys(PRODUCT_MAPPING);
  const foundKey = keys.find(k => k.toUpperCase() === upperSku);
  if (foundKey) {
    return PRODUCT_MAPPING[foundKey];
  }

  // 3. Try fallback variations (removing -CD suffix)
  if (upperSku.endsWith("-CD")) {
    const baseSku = upperSku.substring(0, upperSku.length - 3);
    const foundBase = keys.find(k => k.toUpperCase() === baseSku || k.toUpperCase() === baseSku + "-CD");
    if (foundBase) {
      return PRODUCT_MAPPING[foundBase];
    }
    // Also try checking if the base matches any key without -CD, or with -CD
    const cleanBaseKey = keys.find(k => k.replace(/-CD$/i, "").toUpperCase() === baseSku);
    if (cleanBaseKey) {
      return PRODUCT_MAPPING[cleanBaseKey];
    }
  }

  // 4. Try lookup by ignoring -CD inside key or cleanSku
  const normalizedInput = upperSku.replace(/-CD$/i, "");
  const foundNormalized = keys.find(k => {
    return k.replace(/-CD$/i, "").toUpperCase() === normalizedInput;
  });
  if (foundNormalized) {
    return PRODUCT_MAPPING[foundNormalized];
  }

  // 5. Try split key lookup for complexes (e.g. key has '+')
  if (cleanSku.includes("+")) {
    const parts = cleanSku.split("+").map(p => p.trim());
    const names = parts.map(p => lookupProductName(p));
    return "Combo: " + names.join(" + ");
  }

  return `Sản phẩm ${cleanSku}`;
}
